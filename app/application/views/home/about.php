<h2><i class="fa fa-code"></i> About the #5yrCodeJam</h2>

<p class="intro">
As we celebrate the 5 years of tech community, we want to 
crown the kings and queens of code! Are you up for the challenge? 
</p>
<p>We want to know who has been writing clean, efficient and elegant 
code for the past 5 years ;-) We want to symbolically celebrate the 
5 years of writing code, hopefully good code.
</p>

<p>
#SCICodeJam, run by the School of Computing and Informatics, 
University of Nairobi has teamed up with Angani Ltd, to make this happen. 
Registration for participation is now open. The coding challenges will 
begin on Monday, March 2, 2015 10.00AM and end on March 5, 2015 10.00 AM (yes, 48 hours). 
Registration is open from now till the close of the challenge on Wednesday March 5. 
</p>

<p>
There will be lots of fabulous and enviable prizes to be won for the <em>kings and 
queens of code</em>. And lots of surprise prizes &mdash; you know the serendipity of 
the community. 
</p>


<h2><i class="fa fa-legal"></i> Judging Criteria</h2>
<p>This is what we will be judging:</p>
<ul>
	<li><strong>Correctness</strong> - there’s only one correct output for the challenges.</li>
	<li><strong>Efficiency</strong> - how fast can your code run? (We might actually try and run
	 it on <em>Pentium I</em> machines, not Core i7)</li>
	<li><strong>Elegance</strong> - they say code is poetry. However, this will not be a major 
		criteria but a plus; and will be purely subjective, we can’t help.</li>
</ul>

<h2><i class="fa fa-thumbs-up"></i> A Few Terms and Conditions</h2>
<ul>
<li>For you to be eligible to participate, you <strong>MUST</strong> first have a ticket to 
	the <a href="https://bit.ly/5yrTechBash" target="_blank">#5yrTechBash</a> (or the person 
	representing you, for the case of those under 18 year)</li>
<li>Exception: for those who will not have the bash tickets, you may only participate
 for fun - no winning of any prize (Yes, we want you to be at the party! 
 Community is king). </li>
<li>Anyone, any age (young and old), residing in Kenya today is welcome to participate.</li>
<li>You can use any programming language.</li>
<li>For the minors (under 18) who will win, they will not attend the 
	TechBash because of the age restrictions, but they can have 
	representation by the adults if possible.</li>
<li>Take it easy, nothing too serious here, remember we are in party mode :-)</li>
</ul>

